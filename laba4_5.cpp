#include <iostream>
#include <vector>
#include <ctime>
#include <cstdlib>

using namespace std;

// Стратегии игроков
bool всегда_сотрудничать(int, vector<bool>, vector<bool>) { return true; }
bool всегда_предавать(int, vector<bool>, vector<bool>) { return false; }
bool око_за_око(int раунд, vector<bool>, vector<bool> враг) { 
    return раунд == 0 ? true : враг.back(); 
}

// Игра между двумя стратегиями
void игра(bool (*стратегия1)(int, vector<bool>, vector<bool>), 
          bool (*стратегия2)(int, vector<bool>, vector<bool>), 
          int раунды) {
    
    vector<bool> выбор1, выбор2;
    int очки1 = 0, очки2 = 0;
    
    for (int р = 0; р < раунды; р++) {
        bool ход1 = стратегия1(р, выбор1, выбор2);
        bool ход2 = стратегия2(р, выбор2, выбор1);
        
        выбор1.push_back(ход1);
        выбор2.push_back(ход2);
        
        if (ход1 && ход2) { // Оба сотрудничают
            очки1 += 24; очки2 += 24;
        } else if (!ход1 && !ход2) { // Оба предают
            очки1 += 4; очки2 += 4;
        } else if (ход1 && !ход2) { // 1 сотрудничает, 2 предает
            очки1 += 0; очки2 += 20;
        } else { // 1 предает, 2 сотрудничает
            очки1 += 20; очки2 += 0;
        }
    }
    
    cout << "Игрок 1: " << очки1 << " очков\n";
    cout << "Игрок 2: " << очки2 << " очков\n\n";
}

int main() {
    srand(time(0));
    int раунды = 100 + rand() % 101; // Случайное число раундов 100-200
    
    cout << "Количество раундов: " << раунды << "\n\n";
    
    cout << "1. Всегда сотрудничать vs Всегда предавать:\n";
    игра(всегда_сотрудничать, всегда_предавать, раунды);
    
    cout << "2. Око за око vs Всегда предавать:\n";
    игра(око_за_око, всегда_предавать, раунды);
    
    cout << "3. Око за око vs Всегда сотрудничать:\n";
    игра(око_за_око, всегда_сотрудничать, раунды);
    
    return 0;
}
